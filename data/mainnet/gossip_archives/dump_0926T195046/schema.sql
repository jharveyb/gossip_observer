CREATE TABLE messages(hash UBIGINT NOT NULL, raw VARCHAR NOT NULL);;
CREATE TABLE metadata(hash UBIGINT NOT NULL, "type" UTINYINT NOT NULL, size USMALLINT NOT NULL, orig_node VARCHAR, scid UBIGINT);;
CREATE TABLE timings(hash UBIGINT NOT NULL, collector VARCHAR NOT NULL, recv_peer VARCHAR NOT NULL, recv_peer_hash UBIGINT NOT NULL, recv_timestamp TIMESTAMP WITH TIME ZONE NOT NULL, orig_timestamp TIMESTAMP WITH TIME ZONE);;

